package datatype;

public class Variable {
	 //global variable
	
	public static void main(String[]args){
		
		int a=10;
		int b=5;
	
	
	//arthmetic operator
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a%b);
	System.out.println(a++);
	System.out.println(a);
	System.out.println(a*b);
	
	System.out.println(b++);
	System.out.println(b);
	
	System.out.println(b--);
	System.out.println(b);
	
	System.out.println(--b);
	
	
}
}